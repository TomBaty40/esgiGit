// Déclaration de la fonction d'affichage
function afficherBonjour() {
  console.log("Bonjour, monde !");
}

// Appel de la fonction
afficherBonjour();

// Déclaration d'une variable contenant un message
let message = "C'est une journée ensoleillée.";

// Affichage du message
console.log(message);

// Une boucle simple
for (let i = 0; i < 5; i++) {
  console.log("Itération " + (i + 1));
}

// Déclaration d'un objet
let personne = { nom: "Dupont", prénom: "Jean", âge: 30 };

// Affichage des propriétés de l'objet
console.log("Nom: " + personne.nom);
console.log("Prénom: " + personne.prénom);
console.log("Âge: " + personne.âge);
